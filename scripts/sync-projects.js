#!/usr/bin/env node
/**
 * sync-projects.js — regenerate projects/projects.json from the canonical
 * ZABAL Gamez catalog.
 *
 * Source of truth: the adoptable-projects.json in the ZABAL Gamez repo
 * (https://zabalgames.com/projects renders from the same file). We pull the
 * GitHub raw URL because zabalgames.com itself sits behind Cloudflare and
 * can't be fetched programmatically.
 *
 * Usage:  node scripts/sync-projects.js          (writes the file)
 *         node scripts/sync-projects.js --check   (dry run: report diff only)
 *
 * Requires Node 18+ (global fetch). No dependencies, no build step — run it
 * whenever the upstream catalog changes, then commit projects/projects.json.
 *
 * Note: the upstream catalog has no per-project tech "stack" field and some
 * upstream titles are verbose, so we apply a small local curation layer
 * (scripts/project-overrides.json, keyed by the stable upstream `id`) that
 * adds `stack` chips and cleaner display names. Unmapped ids fall back to the
 * upstream name with no stack chip.
 */

const fs = require('fs');
const path = require('path');

const SOURCE =
  'https://raw.githubusercontent.com/ZAODEVZ/zabalgames/main/data/adoptable-projects.json';
const OUT = path.join(__dirname, '..', 'projects', 'projects.json');
const OVERRIDES = path.join(__dirname, 'project-overrides.json');

// Display metadata per maturity bucket (upstream has no group copy).
const GROUP_META = {
  shipped: { name: 'Shipped', label: 'live', note: 'In production and usable today.' },
  working: { name: 'Working', label: 'in progress', note: 'Active builds — usable, still moving.' },
  early: { name: 'Early-stage', label: 'starters', note: 'Scaffolds and templates ready to fork.' },
  concept: {
    name: 'Concept',
    label: 'to build',
    note: 'Specced and open for a builder to pick up — start from the rails, ship in the open.',
  },
};

const repoPath = (url) =>
  url ? String(url).replace(/^https?:\/\/github\.com\//, '').replace(/\/+$/, '') : undefined;

function loadOverrides() {
  // id -> { name?, stack? } curation layer; survives upstream renames.
  try {
    const raw = JSON.parse(fs.readFileSync(OVERRIDES, 'utf8'));
    delete raw._comment;
    return raw;
  } catch {
    return {};
  }
}

function toProject(p, overrides) {
  const ov = overrides[p.id] || {};
  const out = { name: ov.name || p.name, type: String(p.adopt_type || 'build').toLowerCase() };
  out.desc = p.description || '';
  if (ov.stack) out.stack = ov.stack;
  const repo = repoPath(p.repo);
  if (repo) out.repo = repo;
  else if (p.spec_label) out.spec = p.spec_label;
  return out;
}

function build(upstream, overrides) {
  const order = upstream.maturity_order || ['shipped', 'working', 'early', 'concept'];
  const groups = [];
  for (const maturity of order) {
    const meta = GROUP_META[maturity] || { name: maturity, label: '', note: '' };
    const projects = upstream.projects
      .filter((p) => p.maturity === maturity)
      .map((p) => toProject(p, overrides));
    if (projects.length) groups.push({ ...meta, projects });
  }
  return {
    _comment:
      'Source of truth for /projects. Generated by scripts/sync-projects.js from zabalgames.com (ZABAL Gamez adoptable-projects.json). Run that script to refresh. type: extend | fork | build. Omit "repo" for concept-stage and add "spec".',
    _generated: new Date().toISOString().slice(0, 10),
    _source: SOURCE,
    groups,
  };
}

async function main() {
  const check = process.argv.includes('--check');
  const res = await fetch(SOURCE, { headers: { accept: 'application/json' } });
  if (!res.ok) throw new Error(`Upstream fetch failed: HTTP ${res.status}`);
  const upstream = await res.json();
  const overrides = loadOverrides();
  const next = build(upstream, overrides);

  const total = next.groups.reduce((n, g) => n + g.projects.length, 0);
  const stacksKept = next.groups.reduce(
    (n, g) => n + g.projects.filter((p) => p.stack).length,
    0
  );
  const nextStr = JSON.stringify(next, null, 2) + '\n';

  let prevStr = '';
  try {
    prevStr = fs.readFileSync(OUT, 'utf8');
  } catch {}

  if (check) {
    // Compare content only — ignore the _generated timestamp so a new day
    // alone doesn't report the catalog as stale.
    const stripDate = (s) => {
      try {
        const o = JSON.parse(s);
        delete o._generated;
        return JSON.stringify(o);
      } catch {
        return s;
      }
    };
    const same = stripDate(prevStr) === stripDate(nextStr);
    console.log(
      `[check] upstream: ${total} projects across ${next.groups.length} groups · ${stacksKept} stack chips preserved`
    );
    console.log(same ? '[check] projects.json is up to date.' : '[check] projects.json WOULD change — run without --check to write.');
    process.exit(same ? 0 : 1);
  }

  fs.writeFileSync(OUT, nextStr);
  console.log(
    `Wrote ${path.relative(process.cwd(), OUT)} — ${total} projects, ${next.groups.length} groups, ${stacksKept} stack chips preserved.`
  );
}

main().catch((err) => {
  console.error('sync-projects failed:', err.message);
  process.exit(1);
});
